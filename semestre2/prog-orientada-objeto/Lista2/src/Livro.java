
public class Livro {
	int codigo;
	String titulo;
	String autor;
	String anoPublicacao; 
	String edicao;
	
	public Livro(int codigo, String titulo, String autor, String anoPublicacao, String edicao) {
		this.codigo = codigo;
		this.titulo = titulo;
		this.autor = autor;
		this.anoPublicacao = anoPublicacao;
		this.edicao = edicao;
	}
	
}
